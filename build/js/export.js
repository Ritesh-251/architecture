/**
 * Architect3D — Export Module
 * Exports: PDF (jsPDF), PNG, JPEG, GLTF
 * Depends on: toast.js, Three.js (global), blueprint3d (global)
 */
(function (global) {
  'use strict';

  // ── jsPDF lazy-load ──
  function loadJsPDF() {
    return new Promise((resolve, reject) => {
      if (global.jspdf) { resolve(global.jspdf.jsPDF); return; }
      const s = document.createElement('script');
      s.src = 'https://cdnjs.cloudflare.com/ajax/libs/jspdf/2.5.1/jspdf.umd.min.js';
      s.onload = () => resolve(global.jspdf.jsPDF);
      s.onerror = reject;
      document.head.appendChild(s);
    });
  }

  function getDesignName() {
    const input = document.getElementById('a3d-design-name');
    return (input ? input.value.trim() : '') || 'Architect3D_Design';
  }

  function triggerDownload(dataUrl, fileName) {
    const a = document.createElement('a');
    a.href = dataUrl;
    a.download = fileName;
    document.body.appendChild(a);
    a.click();
    document.body.removeChild(a);
  }

  // ── Export 2D Canvas as PNG ──
  async function exportPNG() {
    const toast = global.A3D && global.A3D.toast;
    const canvas = document.getElementById('floorplanner-canvas');
    if (!canvas) { toast && toast.show('No 2D canvas found', 'error'); return; }
    toast && toast.show('Generating PNG…', 'info');
    try {
      // Render at 2× resolution
      const offscreen = document.createElement('canvas');
      offscreen.width  = canvas.width  * 2;
      offscreen.height = canvas.height * 2;
      const ctx = offscreen.getContext('2d');
      ctx.scale(2, 2);
      ctx.drawImage(canvas, 0, 0);
      const dataUrl = offscreen.toDataURL('image/png');
      triggerDownload(dataUrl, getDesignName() + '_2D.png');
      toast && toast.show('PNG downloaded!', 'success');
    } catch (e) {
      toast && toast.show('PNG export failed: ' + e.message, 'error');
    }
  }

  // ── Export 2D Canvas as PDF ──
  async function exportPDF() {
    const toast = global.A3D && global.A3D.toast;
    toast && toast.show('Generating PDF…', 'info');
    try {
      const canvas = document.getElementById('floorplanner-canvas');
      if (!canvas) throw new Error('No 2D canvas found');
      const jsPDF = await loadJsPDF();
      const imgData = canvas.toDataURL('image/png');
      const pdf = new jsPDF({ orientation: 'landscape', unit: 'px', format: 'a4' });
      const pageW = pdf.internal.pageSize.getWidth();
      const pageH = pdf.internal.pageSize.getHeight();
      pdf.setFontSize(14);
      pdf.text(getDesignName(), 20, 24);
      pdf.setFontSize(9);
      pdf.setTextColor(120);
      pdf.text('Generated by Architect3D · ' + new Date().toLocaleDateString(), 20, 36);
      pdf.addImage(imgData, 'PNG', 20, 48, pageW - 40, pageH - 68);
      pdf.save(getDesignName() + '_floorplan.pdf');
      toast && toast.show('PDF downloaded!', 'success');
    } catch (e) {
      toast && toast.show('PDF export failed: ' + e.message, 'error');
    }
  }

  // ── Export 3D as JPEG screenshot ──
  function exportJPEG() {
    const toast = global.A3D && global.A3D.toast;
    try {
      if (!global.blueprint3d || !global.blueprint3d.three) throw new Error('3D view not active');
      toast && toast.show('Capturing 3D screenshot…', 'info');
      setTimeout(() => {
        const renderer = global.blueprint3d.three.renderer;
        if (!renderer) throw new Error('Renderer not found');
        renderer.render(global.blueprint3d.three.scene, global.blueprint3d.three.camera);
        const dataUrl = renderer.domElement.toDataURL('image/jpeg', 0.95);
        triggerDownload(dataUrl, getDesignName() + '_3D_render.jpg');
        toast && toast.show('3D screenshot saved!', 'success');
      }, 200);
    } catch (e) {
      toast && toast.show('Screenshot failed: ' + e.message, 'error');
    }
  }

  // ── Export 3D as GLTF ──
  async function exportGLTF() {
    const toast = global.A3D && global.A3D.toast;
    toast && toast.show('Exporting GLTF…', 'info');
    try {
      if (!global.blueprint3d || !global.blueprint3d.three) throw new Error('3D view not active');
      // Try to load GLTFExporter if not present
      let GLTFExporter = global.THREE && global.THREE.GLTFExporter;
      if (!GLTFExporter) throw new Error('GLTFExporter not loaded');
      const exporter = new GLTFExporter();
      exporter.parse(global.blueprint3d.three.scene, function (gltf) {
        const blob = new Blob([JSON.stringify(gltf)], { type: 'application/json' });
        const url = URL.createObjectURL(blob);
        triggerDownload(url, getDesignName() + '_3D.gltf');
        URL.revokeObjectURL(url);
        toast && toast.show('GLTF exported!', 'success');
      }, { binary: false });
    } catch (e) {
      toast && toast.show('GLTF export failed: ' + e.message, 'error');
    }
  }

  // ── Export Menu (floating dropdown) ──
  function openMenu() {
    let menu = document.getElementById('a3d-export-menu');
    if (menu) { menu.style.display = menu.style.display === 'none' ? 'block' : 'none'; return; }

    // Find the download button to anchor the menu
    const anchor = document.getElementById('downloadViews') || document.body;
    const rect = anchor.getBoundingClientRect();

    menu = document.createElement('div');
    menu.id = 'a3d-export-menu';
    menu.style.cssText = `
      position:fixed; top:${rect.bottom + 8}px; left:${rect.left}px;
      background:#1C1C2E; border:1px solid rgba(255,255,255,0.12);
      border-radius:12px; padding:6px; z-index:9997;
      box-shadow:0 8px 32px rgba(0,0,0,0.5);
      font-family:'Inter',sans-serif; min-width:200px;
    `;

    const items = [
      { label: '📄  PDF (2D floor plan)',    fn: exportPDF },
      { label: '🖼️  PNG (2D, high-res)',     fn: exportPNG },
      { label: '📸  JPEG (3D screenshot)',    fn: exportJPEG },
      { label: '🧊  GLTF (3D model)',         fn: exportGLTF },
    ];

    items.forEach(({ label, fn }) => {
      const btn = document.createElement('button');
      btn.style.cssText = `
        display:block;width:100%;text-align:left;padding:10px 14px;
        background:none;border:none;border-radius:8px;
        color:#F0F0F5;font-size:13px;font-weight:500;cursor:pointer;
        transition:background 0.15s;
      `;
      btn.textContent = label;
      btn.onmouseenter = () => btn.style.background = 'rgba(255,255,255,0.06)';
      btn.onmouseleave = () => btn.style.background = 'none';
      btn.onclick = () => { menu.style.display = 'none'; fn(); };
      menu.appendChild(btn);
    });

    document.body.appendChild(menu);
    setTimeout(() => document.addEventListener('click', function handler(e) {
      if (!menu.contains(e.target)) { menu.style.display = 'none'; document.removeEventListener('click', handler); }
    }), 10);
  }

  global.A3D = global.A3D || {};
  global.A3D.export = { exportPDF, exportPNG, exportJPEG, exportGLTF, openMenu };

})(window);
